CREATE OR REPLACE TRIGGER "SWFT01UA1"."CREATE_RALF_APPLICATION" 
BEFORE INSERT
ON TB_RALF_APPLICATION
REFERENCING NEW AS NEW OLD AS OLD
FOR EACH ROW
BEGIN
   :NEW.CREATE_DATE := SYSDATE;

   EXCEPTION
     WHEN OTHERS THEN
       -- Consider logging the error and then re-raise
       RAISE;
END;



/
ALTER TRIGGER "SWFT01UA1"."CREATE_RALF_APPLICATION" ENABLE;